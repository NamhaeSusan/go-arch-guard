func TestNoSideEffectCallInCoreDeniedAndAllowedCallsAreConfigurable(t *testing.T) {
	ctx := purityContext(t, map[string]string{
		"internal/domain/order/core/model/order.go": `package model

import "time"

type Order struct{ CreatedAt time.Time }

func NewOrder(now time.Time) Order {
	return Order{CreatedAt: time.Now()}
}
`,
	})

	got := dependency.NewNoSideEffectCallInCore(
		dependency.WithAllowedCalls("time.Now"),
	).Check(ctx)
	if len(got) != 0 {
		t.Fatalf("allowed time.Now should pass, got %+v", got)
	}

	got = dependency.NewNoSideEffectCallInCore(
		dependency.WithDeniedCalls("time.Time"),
	).Check(ctx)
	if len(got) != 0 {
		t.Fatalf("non-call time.Time type should not be flagged, got %+v", got)
	}
}

func TestNoSideEffectCallInCoreInspectedLayersAreConfigurable(t *testing.T) {
	ctx := purityContext(t, map[string]string{
		"internal/domain/order/app/service.go": `package app

import "time"

func Now() time.Time { return time.Now() }
`,
	})

	got := dependency.NewNoSideEffectCallInCore().Check(ctx)
	if len(got) != 0 {
		t.Fatalf("app layer should not be checked by default, got %+v", got)
	}

	got = dependency.NewNoSideEffectCallInCore(
		dependency.WithInspectedLayers("app"),
	).Check(ctx)
	assertPurityViolation(t, got, "time.Now")
}

func TestNoSideEffectCallInCoreModuleMismatchEmitsMetaWarning(t *testing.T) {
	root := t.TempDir()
	writePurityFile(t, filepath.Join(root, "go.mod"), "module example.com/shop\n\ngo 1.25.0\n")
	writePurityFile(t, filepath.Join(root, "internal/domain/order/core/model/order.go"), `package model

import "time"

func Now() time.Time { return time.Now() }
`)
	pkgs, err := analyzer.Load(root, "internal/...")
	if err != nil {
		t.Fatal(err)
	}
	ctx := core.NewContext(pkgs, "example.com/other", root, presets.DDD(), nil)

	got := dependency.NewNoSideEffectCallInCore().Check(ctx)

	assertViolationCount(t, got, "meta.no-matching-packages", 1)
	assertNoPurityViolation(t, got)
}

func TestNoSideEffectCallInCoreSkipsTestsGeneratedAndExcludedFiles(t *testing.T) {
	ctx := purityContextWithExclude(t, map[string]string{
		"internal/domain/order/core/model/order_test.go": `package model

import "time"

func TestNow() { _ = time.Now() }
`,
		"internal/domain/order/core/model/generated.go": `// Code generated by test. DO NOT EDIT.
package model

import "time"

func GeneratedNow() time.Time { return time.Now() }
`,
		"internal/domain/order/core/model/excluded.go": `package model

import "time"

func ExcludedNow() time.Time { return time.Now() }
`,
	}, []string{"internal/domain/order/core/model/excluded.go"})

	got := dependency.NewNoSideEffectCallInCore().Check(ctx)
	if len(got) != 0 {
		t.Fatalf("tests, generated files, and excluded files should pass, got %+v", got)
	}
}

