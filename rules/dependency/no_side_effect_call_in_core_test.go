func TestNoSideEffectCallInCoreSpec(t *testing.T) {
	spec := dependency.NewNoSideEffectCallInCore(dependency.WithSeverity(core.Error)).Spec()

	if spec.ID != "purity.no-side-effect-call-in-core" {
		t.Fatalf("ID = %q, want purity.no-side-effect-call-in-core", spec.ID)
	}
	if spec.DefaultSeverity != core.Error {
		t.Fatalf("DefaultSeverity = %v, want Error", spec.DefaultSeverity)
	}
	got := spec.ViolationIDs()
	if len(got) != 1 || got[0] != "purity.no-side-effect-call-in-core" {
		t.Fatalf("ViolationIDs() = %v, want [purity.no-side-effect-call-in-core]", got)
	}
}

func TestNoSideEffectCallInCoreFlagsDefaultDeniedCalls(t *testing.T) {
	ctx := purityContext(t, map[string]string{
		"internal/domain/order/core/model/order.go": `package model

import (
	"crypto/rand"
	"log"
	mathrand "math/rand"
	"net/http"
	"os"
	"time"
)

type Order struct {
	ID        string
	CreatedAt time.Time
}

func NewOrder(id string) Order {
		log.Println("creating order")
		_ = os.Getenv("SHOP_CURRENCY")
		_, _ = os.ReadFile("seed")
		_ = os.WriteFile("seed", nil, 0o644)
		_, _ = os.Create("seed")
		_ = os.Remove("seed")
		_, _ = http.Get("https://example.com")
		_, _ = http.DefaultClient.Get("https://example.com")
		_ = mathrand.Int()
		_, _ = rand.Read(nil)
		return Order{ID: id, CreatedAt: time.Now()}
	}
	`,
	})

	got := dependency.NewNoSideEffectCallInCore().Check(ctx)

	for _, want := range []string{
		"log.Println",
		"os.ReadFile",
		"os.WriteFile",
		"os.Create",
		"os.Remove",
		"net/http.Get",
		"net/http.(*Client).Get",
		"math/rand.Int",
		"crypto/rand.Read",
		"time.Now",
	} {
		assertPurityViolation(t, got, want)
	}
	assertPurityViolation(t, got, "os.Getenv")
}

