func TestNoSideEffectCallInCoreIsOptInForRecommendedDDD(t *testing.T) {
	for _, rule := range presets.RecommendedDDD().Rules() {
		if rule.Spec().ID == "purity.no-side-effect-call-in-core" {
			t.Fatalf("purity.no-side-effect-call-in-core must stay opt-in, found in RecommendedDDD()")
		}
	}
}

func purityContext(t *testing.T, files map[string]string) *core.Context {
	t.Helper()
	return purityContextWithExclude(t, files, nil)
}

func purityContextWithExclude(t *testing.T, files map[string]string, exclude []string) *core.Context {
	t.Helper()
	root := t.TempDir()
	writePurityFile(t, filepath.Join(root, "go.mod"), "module example.com/shop\n\ngo 1.25.0\n")
	for name, content := range files {
		writePurityFile(t, filepath.Join(root, name), content)
	}
	pkgs, err := analyzer.Load(root, "internal/...")
	if err != nil {
		t.Fatal(err)
	}
	return core.NewContext(pkgs, "example.com/shop", root, presets.DDD(), exclude)
}

func writePurityFile(t *testing.T, path, content string) {
	t.Helper()
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		t.Fatal(err)
	}
}

func assertPurityViolation(t *testing.T, violations []core.Violation, want string) {
	t.Helper()
	for _, v := range violations {
		if v.Rule == "purity.no-side-effect-call-in-core" && strings.Contains(v.Message, want) {
			if v.DefaultSeverity != core.Warning || v.EffectiveSeverity != core.Warning {
				t.Fatalf("severity = default %v effective %v, want Warning/Warning", v.DefaultSeverity, v.EffectiveSeverity)
			}
			return
		}
	}
	t.Fatalf("expected purity.no-side-effect-call-in-core violation containing %q, got %+v", want, violations)
}

func assertNoPurityViolation(t *testing.T, violations []core.Violation) {
	t.Helper()
	for _, v := range violations {
		if v.Rule == "purity.no-side-effect-call-in-core" {
			t.Fatalf("unexpected purity violation: %+v", v)
		}
	}
}

func assertViolationCount(t *testing.T, violations []core.Violation, rule string, want int) {
	t.Helper()
	var got int
	for _, v := range violations {
		if v.Rule == rule {
			got++
		}
	}
	if got != want {
		t.Fatalf("%s violation count = %d, want %d; got %+v", rule, got, want, violations)
	}
}
